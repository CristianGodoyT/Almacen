    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre;
        int precio,codbarra;
        
        
        try {
            nombre = txtNombre.getText();
            precio = Integer.parseInt(txtPrecio.getText());
            codbarra = Integer.parseInt(txtCodBarra.getText());
            
            PreparedStatement pps = cn.prepareStatement("INSERT INTO producto VALUES (?,?,?,?);");
            pps.setInt(1, codbarra);
            pps.setString(2, nombre);
            pps.executeUpdate();
            cn.prepareStatement("INSERT INTO producto_precio VALUES (?,?,?,?,?)");
            pps.setInt(3, precio);
            pps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(ProductosAgregar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed


